export function getTranslations(lang = 'ru') {
    return translations[lang] || translations.ru;
}

/**
 * Gets configuration for a specific chain (lesson).
 * @param {string} lang - Language code
 * @param {number} chainId - Chain ID (1 or 2)
 */
export function getChainConfig(lang = 'ru', chainId = 1) {
    const t = getTranslations(lang);
    return t.chains[chainId] || t.chains[1];
}

/**
 * Gets questions for a specific language and chain.
 * Maintained for backward compatibility but prefers chain-aware usage.
 */
export function getQuestions(lang = 'ru', chainId = 1) {
    return getChainConfig(lang, chainId).questions;
}
